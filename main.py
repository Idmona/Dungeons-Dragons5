OUTPUT_FOLDER = "cards"
NUM_CARDS = 10


def to_symbols(text):
    for char, replacement in LETTERS.items():
        text = text.replace(char, replacement)
    return text


def generate_context(fake, skills):
    runic_skills = [to_symbols(skill) for skill in random.sample(skills, 3)]
    return {
        "first_name": fake.first_name(),
        "last_name": fake.last_name(),
        "job": fake.job(),
        "town": fake.city(),
        "strength": random.randint(3, 18),
        "agility": random.randint(3, 18),
        "endurance": random.randint(3, 18),
        "intelligence": random.randint(3, 18),
        "luck": random.randint(3, 18),
        "skill_1": runic_skills[0],
        "skill_2": runic_skills[1],
        "skill_3": runic_skills[2],
    }


def save_card(fake, skills, card_number):
    context = generate_context(fake, skills)
    output_filename = os.path.join(
        OUTPUT_FOLDER, f"charsheet_result_{card_number}.svg")
    file_operations.render_template("charsheet.svg", output_filename, context)


def main():
    if not os.path.exists(OUTPUT_FOLDER):
        os.makedirs(OUTPUT_FOLDER)

    fake = Faker("ru_RU")

    for i in range(1, NUM_CARDS + 1):
        save_card(fake, SKILLS, i)


if __name__ == "__main__":
    main()
